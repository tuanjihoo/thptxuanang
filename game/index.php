            function lop(arr) {
            	arr = arr.split('');
            	var num = 0;
		      	for (var i = 0; i < arr.length; i++) {
		      		if (isInt(parseInt(arr[i]))) {
		      			num = parseInt(num) + parseInt(arr[i]);
		      		}
		      	};

		      	if (num.toString().length > 1) {
		      		return lop(num.toString());
		      	} else {
		      		return num.toString();
		      	}
            }

