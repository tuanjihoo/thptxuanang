            function convertImgCard(fbId) {
				html2canvas($('#export_image_card'), {
		            onrendered: function(canvas) {
		                $.ajax({
		                	url: "export.php", 
		                	dataType: 'JSON',
		                	type: 'POST',
		                	data: {
		                		img: canvas.toDataURL("image/jpg"),
		                		id: fbId,
		                		type: 'card'
		                	},
		                	success: function(data){
					        	$(".meta-img").attr('content', 'http://' + window.location.host + '/game/' + data);
					        	setTimeout(function() {
					        		$(".share-btn-card").removeAttr('disabled');
					        	}, 1000);
						    }
						});
		            }
		        });
			}

