			function autoDown(fbId, img, type) {
                $.ajax({
                	url: "autodown.php", 
                	dataType: 'JSON',
                	type: 'POST',
                	data: {
                		img: img,
                		id: fbId,
                		type: type
                	},
                	success: function(data){
			        	if (data.status) {
			        		if (type == 'card') {
			        			$(".card-avatar").html('<img src="' + data.img + '" class="img-responsive">');
			        			setTimeout(function() {
						       		$(".nen-bg .loadingImg").remove();
						       		$('#tab-input-card').fadeOut(0);
						       		$('#tab-export-card').fadeIn(1000);
						       		setTimeout(function() {
						       			convertImgCard(fbId);
						       		}, 2000);
						       	}, 5000);
			        		}
			        		if (type == 'chieucao') {
			        			$('#export_image').css('background-image', 'url(' + data.img + ')');
			        			$(".share-btn").attr('data-cover', 'http://' + window.location.host + '/game/' + data.img);
			        			setTimeout(function() {
						       		$(".loadingImg").remove();
						       		$('.build-img').fadeIn(1000);
						       		setTimeout(function() {
						       			convertImg(fbId);
						       		}, 2000);
						       	}, 5000);
			        		}

			        	}
				    }
				});
			}

